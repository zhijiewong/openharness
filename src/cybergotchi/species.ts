import type { Emotion } from './types.js';

export interface SpeciesDef {
  name: string;
  label: string;
  traitHint: string;
  // frames[emotion] = array of 8-line strings, each line exactly 14 chars wide
  // Use {E} as placeholder for eye injection (3 chars: "x x")
  frames: Record<Emotion, string[][]>;
}

// Helper: pad string to exactly 14 chars
function p(s: string): string {
  if (s.length >= 14) return s.slice(0, 14);
  return s + ' '.repeat(14 - s.length);
}

// Build frames: each frame is 8 lines of 14 chars
// For single-frame emotions, supply one frame; for animated, supply multiple
function f(...lines: string[]): string[] {
  return lines.map(p);
}

